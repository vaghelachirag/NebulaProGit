package com.nebulacompanies.ibo.model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class BvList {

    @SerializedName("Id")
    @Expose
    private int Id;

    @SerializedName("BVMax")
    @Expose
    private Long bvmax;

    @SerializedName("BVMin")
    @Expose
    private Long bvmin;

    @SerializedName("Differentials")
    @Expose
    private double differentials;

    @SerializedName("Commission")
    @Expose
    private double commission;


    public Long getBvmax() {
        return bvmax;
    }

    public void setBvmax(Long bvmax) {
        this.bvmax = bvmax;
    }

    public Long getBvmin() {
        return bvmin;
    }

    public void setBvmin(Long bvmin) {
        this.bvmin = bvmin;
    }

    public double getDifferentials() {
        return differentials;
    }

    public void setDifferentials(double differentials) {
        this.differentials = differentials;
    }

    public double getCommission() {
        return commission;
    }

    public void setCommission(double commission) {
        this.commission = commission;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }
}