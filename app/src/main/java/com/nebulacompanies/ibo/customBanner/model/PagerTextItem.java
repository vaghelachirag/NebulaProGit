package com.nebulacompanies.ibo.customBanner.model;


import android.text.SpannableString;

import java.io.Serializable;

/**
 * Created by RxRead on 2015/9/24.
 */
public class PagerTextItem implements Serializable {
	private int position;
	private String name;
	private String imageUrl;

	public SpannableString getDesc() {
		return desc;
	}

	public void setDesc(SpannableString desc) {
		this.desc = desc;
	}

	private SpannableString desc;

	public void setPosition(int position) {
		this.position = position;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}


}
