package com.nebulacompanies.ibo.ecom.model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class EcomOfferModelList
{
    @SerializedName("Id")
    @Expose
    private Integer Id;

    @SerializedName("Name")
    @Expose
    private String offerName;
    @SerializedName("Title")
    @Expose
    private String offerTitle;
    @SerializedName("content")
    @Expose
    private String offerContent;
    @SerializedName("ImageUrl")
    @Expose
    private String offerImage;
    @SerializedName("StartDateTime")
    @Expose
    private Long offerStartDate;

    @SerializedName("EndDateTime")
    @Expose
    private Long offerEndDate;


    @SerializedName("IsDelete")
    @Expose
    private Boolean isDelete;

    public Integer getId() {
        return Id;
    }

    public void setId(Integer Id) {
        this.Id = Id;
    }

    public String getOfferName() {
        return offerName;
    }

    public void setOfferName(String offerName) {
        this.offerName = offerName;
    }

    public String getOfferTitle() {
        return offerTitle;
    }

    public void setOfferTitle(String offerTitle) {
        this.offerTitle = offerTitle;
    }

    public String getOfferContent() {
        return offerContent;
    }

    public void setOfferContent(String offerContent) {
        this.offerContent = offerContent;
    }

    public String getOfferImage() {
        return offerImage;
    }

    public void setOfferImage(String offerImage) {
        this.offerImage = offerImage;
    }

    public Long getOfferStartDate() {
        return offerStartDate;
    }

    public void setOfferStartDate(Long offerStartDate) {
        this.offerStartDate = offerStartDate;
    }

    public Long getOfferEndDate() {
        return offerEndDate;
    }

    public void setOfferEndDate(Long offerEndDate) {
        this.offerEndDate = offerEndDate;
    }


    public Boolean getDelete() {
        return isDelete;
    }

    public void setDelete(Boolean delete) {
        isDelete = delete;
    }
}
