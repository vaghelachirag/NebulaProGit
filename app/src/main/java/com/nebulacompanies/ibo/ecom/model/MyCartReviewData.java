package com.nebulacompanies.ibo.ecom.model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.Collection;
import java.util.List;

public class MyCartReviewData {

    @SerializedName("Count")
    @Expose
    private Integer cartCount;

    @SerializedName("OutOfStockProducts")
    @Expose
    private List<MyCartReview> OutOfStockProducts = null;


    public Integer getCartCount() {
        return cartCount;
    }

    public void setCartCount(Integer cartCount) {
        this.cartCount = cartCount;
    }

    public List<MyCartReview> getOutOfStockProducts() {
        return OutOfStockProducts;
    }

    public void setOutOfStockProducts(List<MyCartReview> outOfStockProducts) {
        OutOfStockProducts = outOfStockProducts;
    }
}
