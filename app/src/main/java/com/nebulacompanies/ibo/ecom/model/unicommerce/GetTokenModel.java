package com.nebulacompanies.ibo.ecom.model.unicommerce;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class GetTokenModel {

    @SerializedName("username")
    @Expose
    private Integer uniUserName;

    @SerializedName("password")
    @Expose
    private Integer uniPassword;

    @SerializedName("grant_type")
    @Expose
    private Integer uniGrantType;

    @SerializedName("client_id")
    @Expose
    private Integer uniClientID;


    public Integer getUniUserName() {
        return uniUserName;
    }

    public void setUniUserName(Integer uniUserName) {
        this.uniUserName = uniUserName;
    }

    public Integer getUniPassword() {
        return uniPassword;
    }

    public void setUniPassword(Integer uniPassword) {
        this.uniPassword = uniPassword;
    }

    public Integer getUniGrantType() {
        return uniGrantType;
    }

    public void setUniGrantType(Integer uniGrantType) {
        this.uniGrantType = uniGrantType;
    }

    public Integer getUniClientID() {
        return uniClientID;
    }

    public void setUniClientID(Integer uniClientID) {
        this.uniClientID = uniClientID;
    }


    @Override
    public String toString() {
        return "GetTokenModel{" +
                "uniUserName=" + uniUserName +
                ", uniPassword=" + uniPassword +
                ", uniGrantType=" + uniGrantType +
                ", uniClientID=" + uniClientID +
                '}';
    }
}
