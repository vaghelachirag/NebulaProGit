package com.nebulacompanies.ibo.ecom.model.unicommerce;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.io.Serializable;

public class ShippingPackagesModel implements Serializable {

    @SerializedName("code")
    @Expose
    private String code;

    @SerializedName("channel")
    @Expose
    private String channel;

    @SerializedName("status")
    @Expose
    private String statusOrderNew;


    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public String getStatusOrderNew() {
        return statusOrderNew;
    }

    public void setStatusOrderNew(String statusOrderNew) {
        this.statusOrderNew = statusOrderNew;
    }
}
